    <h2>Passo a Passo</h2>
    <h3>1. Prepare os Baldes</h3>
    <p>
      Utilize dois baldes com furos no fundo para permitir a passagem de líquidos. Eles ficarão
      empilhados sobre um terceiro balde coletor, sem furos, para armazenar o chorume.
    </p>

    <h3>2. Adicione os Resíduos</h3>
    <p>
      No balde superior, adicione resíduos orgânicos como cascas de frutas, legumes e restos de
      vegetais. Evite adicionar carnes, laticínios e alimentos gordurosos para não atrair pragas ou
      gerar odores fortes.
    </p>

    <h3>3. Cubra com Serragem ou Palha</h3>
    <p>
      Após adicionar os resíduos, cubra-os com uma camada de serragem ou palha. Isso ajuda a
      controlar a umidade, reduzir odores e acelerar o processo de decomposição.
    </p>

    <h3>4. Ciclo dos Baldes</h3>
    <p>
      Quando o balde superior estiver cheio, mova-o para o meio da pilha e posicione o balde
      intermediário na parte superior. Isso garante que os resíduos continuem decompondo enquanto
      você adiciona novos.
    </p>

    <h3>5. Recolha o Chorume</h3>
    <p>
      No balde coletor (inferior), acumulará o chorume. Este líquido pode ser diluído (1 parte de
      chorume para 10 partes de água) e usado como fertilizante natural para suas plantas.
    </p>

    <h3>6. Utilize o Adubo</h3>
    <p>
      Após cerca de dois meses, o material nos baldes superiores terá se transformado em adubo
      orgânico, pronto para uso em hortas, vasos e jardins.
    </p>

    <h2>Dicas Finais</h2>
    <ul>
      <li>Certifique-se de que os baldes fiquem bem tampados para evitar moscas e odores.</li>
      <li>Mantenha a composteira em local sombreado e ventilado.</li>
    </ul>
